function isTimePassed(timeStr) {
  const match = timeStr.match(/^(\d{1,2}):(\d{2})$/);
  if (!match) return false;
  const now = new Date();
  const nowMins = now.getHours() * 60 + now.getMinutes();
  return parseInt(match[1]) * 60 + parseInt(match[2]) < nowMins;
}

